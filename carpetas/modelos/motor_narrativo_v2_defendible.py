def _softmax(z):
    z = np.asarray(z, dtype=float)
    z = z - np.max(z, axis=1, keepdims=True)
    exp_z = np.exp(z)
    return exp_z / exp_z.sum(axis=1, keepdims=True)

def predict_narrative_v2(df):
    """
    Predice nivel narrativo TRUNA-ELE a partir de un DataFrame con índices TRUNAJOD.
    Retorna una copia del DataFrame con columnas agregadas.
    """
    features = MODEL_PARAMS["features"]
    missing = [c for c in features if c not in df.columns]
    if missing:
        raise ValueError(
            "El archivo no contiene todos los índices TRUNAJOD requeridos para el Motor Narrativo v2. "
            f"Faltan: {missing[:10]}" + ("..." if len(missing) > 10 else "")
        )

    X = df[features].copy()
    X = X.apply(pd.to_numeric, errors="coerce").to_numpy(dtype=float)

    imputer_statistics = np.asarray(MODEL_PARAMS["imputer_statistics"], dtype=float)
    inds = np.where(np.isnan(X))
    X[inds] = np.take(imputer_statistics, inds[1])

    mean = np.asarray(MODEL_PARAMS["scaler_mean"], dtype=float)
    scale = np.asarray(MODEL_PARAMS["scaler_scale"], dtype=float)
    Xs = (X - mean) / scale

    coef = np.asarray(MODEL_PARAMS["coef"], dtype=float)
    intercept = np.asarray(MODEL_PARAMS["intercept"], dtype=float)

    logits = Xs @ coef.T + intercept
    probs = _softmax(logits)

    classes = MODEL_PARAMS["classes"]
    label_mapping = MODEL_PARAMS["label_mapping"]

    pred_idx = np.argmax(probs, axis=1)
    pred_numeric = np.asarray([classes[i] for i in pred_idx], dtype=int)
    pred_label = [label_mapping[str(x)] for x in pred_numeric]
    confidence = probs.max(axis=1) * 100

    sorted_probs = np.sort(probs, axis=1)
    margin = (sorted_probs[:, -1] - sorted_probs[:, -2]) * 100

    transition = []
    for m in margin:
        if m < 8:
            transition.append("Alta incertidumbre / zona de transición")
        elif m < 18:
            transition.append("Transición probable")
        else:
            transition.append("Clasificación relativamente estable")

    out = df.copy()
    out["motor_utilizado"] = "TRUNA-ELE Narrativo v2 Defendible"
    out["nivel_estimado_v2_num"] = pred_numeric
    out["nivel_estimado_v2"] = pred_label
    out["confianza_v2"] = np.round(confidence, 1)
    out["margen_decision_v2"] = np.round(margin, 1)
    out["zona_transicion_v2"] = transition

    for i, cls in enumerate(classes):
        out[f"Prob_v2_{label_mapping[str(cls)]}"] = np.round(probs[:, i] * 100, 1)

    return out

